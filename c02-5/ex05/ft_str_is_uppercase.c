int	ft_str_is_uppercase(char *str)
{
	int value;

	value = 1;
	while (*str)
	{
		if (!(*str >= 'A' && *str <= 'Z'))
		{
			value = 0;
			break ;
		}
		str++;
	}
	return (value);
}
