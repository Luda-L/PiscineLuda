int	ft_str_is_lowercase(char *str)
{
	int value;

	value = 1;
	while (*str)
	{
		if (!(*str >= 'a' && *str <= 'z'))
		{
			value = 0;
			break ;
		}
		str++;
	}
	return (value);
}
